
package Ej5_Cuadrilatero;

public class Cuadrilatero {

    private float punto1_x;
    private float punto1_y;
    private float punto2_x;
    private float punto2_y;
    private float punto3_x;
    private float punto3_y;
    private float punto4_x;
    private float punto4_y;

    public Cuadrilatero(float punto1_x, float punto1_y, float punto2_x, float punto2_y, float punto3_x, float punto3_y, float punto4_x, float punto4_y) {
        this.punto1_x = punto1_x;
        this.punto1_y = punto1_y;
        this.punto2_x = punto2_x;
        this.punto2_y = punto2_y;
        this.punto3_x = punto3_x;
        this.punto3_y = punto3_y;
        this.punto4_x = punto4_x;
        this.punto4_y = punto4_y;
    }

    public float getPunto1_x() {
        return punto1_x;
    }

    public void setPunto1_x(float punto1_x) {
        this.punto1_x = punto1_x;
    }

    public float getPunto1_y() {
        return punto1_y;
    }

    public void setPunto1_y(float punto1_y) {
        this.punto1_y = punto1_y;
    }

    public float getPunto2_x() {
        return punto2_x;
    }

    public void setPunto2_x(float punto2_x) {
        this.punto2_x = punto2_x;
    }

    public float getPunto2_y() {
        return punto2_y;
    }

    public void setPunto2_y(float punto2_y) {
        this.punto2_y = punto2_y;
    }

    public float getPunto3_x() {
        return punto3_x;
    }

    public void setPunto3_x(float punto3_x) {
        this.punto3_x = punto3_x;
    }

    public float getPunto3_y() {
        return punto3_y;
    }

    public void setPunto3_y(float punto3_y) {
        this.punto3_y = punto3_y;
    }

    public float getPunto4_x() {
        return punto4_x;
    }

    public void setPunto4_x(float punto4_x) {
        this.punto4_x = punto4_x;
    }

    public float getPunto4_y() {
        return punto4_y;
    }

    public void setPunto4_y(float punto4_y) {
        this.punto4_y = punto4_y;
    }
    
    
    
}
